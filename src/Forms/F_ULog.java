        c.remplirTableau();
        TableRowSorter<TableModel> ordre = new TableRowSorter<TableModel>(c.getDtm());
        TabUlog.setRowSorter(ordre);
        TabUlog.setModel(c.getDtm());// TODO add your handling code here:
