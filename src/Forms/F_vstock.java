    public void remplirProduit() {
        String req = "Select t_article.design from t_article";
        try {
            st = new ConnectDataBase().getConnetion().createStatement();
            rs = st.executeQuery(req);
            while (rs.next()) {
                cmbprod.addItem(rs.getString(1));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur de remplissage de produit", "JStore : Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

