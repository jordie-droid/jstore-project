    public int numrow() {
        int r = 0;
        try {
            String rq = "SELECT max(numstock) FROM t_entrer";
            st = new ConnectDataBase().getConnetion().createStatement();
            rs = st.executeQuery(rq);
            if (rs.next()) {
                r = 0 + rs.getInt(1);
            }
        } catch (Exception ex) {
            System.out.print(ex);
        }
        return r + 1;
    }

