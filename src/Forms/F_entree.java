    public void RemplirArticle() {
        try {
            String reqver = "SELECT * FROM t_article";
            st = new ConnectDataBase().getConnetion().createStatement();
            rs = st.executeQuery(reqver);
            while (rs.next()) {
                Zart.addItem(rs.getString("design"));
            }
        } catch (SQLException ex) {
            System.out.print(ex);
        }
    }

